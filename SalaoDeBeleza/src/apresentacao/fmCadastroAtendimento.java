    private void salvaHistorico(String descricao)
    {
        IAgendamentoHistoricoDAO dao = new AgendamentoHistoricoDAO();
        AgendamentoHistorico agendamentoHistorico = new AgendamentoHistorico(Sessao.getIdUsuario(), descricao);
        dao.adiciona(agendamentoHistorico);
    }    
    private void btFinalizarAtendimentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFinalizarAtendimentoActionPerformed
        // TODO add your handling code here:
        int valor = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Finalizar o atendimento?", "Sistema Salão de Beleza", 1);
        if (valor ==0){
            IAgendamentoDAO dao = new AgendamentoDAO();
            Agendamento agendamento = dao.buscarPorID(Integer.parseInt(txtCodigo.getText()));
            agendamento.setIndFinalizado(true);
            dao.atualiza(agendamento);

            Integer idAgendamento = agendamento.getId();
            Integer idFuncionario = agendamento.getIdFuncionario();
            Float Valor = (agendamento.getPorcentagemComissao()/100.0f) * agendamento.getValor();


            Comissao comissao = new Comissao(idFuncionario, idAgendamento, Valor);
            IComissaoDAO comissaodao = new ComissaoDAO();

            comissaodao.adiciona(comissao);
            JOptionPane.showMessageDialog(null, "Atendimento finalizado!");
            salvaHistorico("Atendimento Finalizado");
            this.dispose();
        }
        
        
    }//GEN-LAST:event_btFinalizarAtendimentoActionPerformed


