    private void geraDadosComboCliente()
    {
        IClienteDAO dao = new ClienteDAO();
        
        
        for (Cliente c: dao.listarTodos())
        {
            cbNomeCliente.addItem(c);
        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int valor = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja salvar?", "Sistema Salão de Beleza", 1);
        if (valor ==0)
        {
            Integer IdCliente;
            Integer IdFuncionario;
            Calendar Data;
            String Descricao;
            float Valor;
            String Observacao;
            int PorcentagemComissao;
            
            Cliente cliente = (Cliente) cbNomeCliente.getSelectedItem();
            IdCliente = cliente.getId();
            Funcionario funcionario = (Funcionario)cbFuncionario.getSelectedItem();
            IdFuncionario = funcionario.getId();

            Data = txtData.getCalendar();

            String Horario = (String) cbHorario.getSelectedItem();

            Descricao = txtDescricao.getText();

            Valor = Float.parseFloat(txtValor.getText());

            Observacao = txtObservacao.getText();

            PorcentagemComissao = Integer.parseInt(txtPorcentagemComissao.getText());
            IAgendamentoDAO dao = new AgendamentoDAO();
            if (txtCodigo.getText() == "00000") {
                Agendamento agendamento = new Agendamento(IdCliente, IdFuncionario, Data, Horario, Descricao, Valor, Observacao, PorcentagemComissao) ;
                dao.adiciona(agendamento);
                JOptionPane.showMessageDialog(null, "Os dados foram gravados");
                salvaHistorico("Atendiemento criado");

            }else
            {
                Agendamento agendamento = new Agendamento(Integer.parseInt(txtCodigo.getText()),IdCliente, IdFuncionario, Data, Horario, Descricao, Valor, Observacao, PorcentagemComissao) ;
                dao.atualiza(agendamento);
                JOptionPane.showMessageDialog(null, "Os dados foram atualizados");
                salvaHistorico("Atendimento atualizado");
            
            }
            
            //limpar();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

