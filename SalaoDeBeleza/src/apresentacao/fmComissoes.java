    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int valor = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja calcular comissoes?", "Sistema Sal√£o de Beleza", 1);
        if (valor ==0){
            IComissaoDAO dao = new ComissaoDAO();
            Funcionario func = (Funcionario) cbFuncionario.getSelectedItem();
            dao.listarComFiltro(func.getId());
            Float valortotal  = (float) 0;
            for (Comissao c: dao.listarComFiltro(func.getId()))
            {
                valortotal += c.getValor();
                dao.SetCalcular(c.getId());
            }

            IFuncionarioDAO funcionariodao = new FuncionarioDAO();
            funcionariodao.atualizaValorAPagar(valortotal, func.getId());
            JOptionPane.showMessageDialog(null, "Comissoes calculadas!");
            txtValorAPagar.setText(String.valueOf(func.getValorComissaoAPagar()));
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void insereListagemNaTabela(List<Comissao> comissoes)
    {
        tbComissoes.removeAll();
        DefaultTableModel modelo = (DefaultTableModel) tbComissoes.getModel();

        for (Comissao c: comissoes)
        {
             modelo.addRow(new Object[]{c.getId(), c.getIdAgendamento(), c.getValor()});
        }   

    }

