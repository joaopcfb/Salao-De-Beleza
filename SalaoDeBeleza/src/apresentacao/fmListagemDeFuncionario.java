    private void insereListagemNaTabela(List<Funcionario> funcionarios)
    {
        IFuncionarioFuncaoDAO funcaodao = new FuncionarioFuncaoDAO();
        
        //tbListagemFunc.removeAll();
        DefaultTableModel modelo = (DefaultTableModel) tbListagemFunc.getModel();
        deletaTodasAsLinhas(modelo);
        for (Funcionario f: funcionarios)
        {
            modelo.addRow(new Object[]{f.getId(), f.getNome(), f.getCPF(), funcaodao.buscarPorID(f.getIdFuncao()).getDescricao()});
        }
    }
    
    public static void deletaTodasAsLinhas(final DefaultTableModel model) {
    for( int i = model.getRowCount() - 1; i >= 0; i-- ) {
        model.removeRow(i);
    }
}
