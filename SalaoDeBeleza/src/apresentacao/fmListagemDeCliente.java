    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        IClienteDAO dao = new ClienteDAO();
        String campo = (String)cbCampo.getSelectedItem();
        String termo = txtTermo.getText();
        List<Cliente> clientes = new ArrayList<Cliente>();

        if (campo == "Codigo") {
            clientes = dao.listarComFiltro(Integer.parseInt(termo), null, null, null);
        }
        else if (campo == "Nome") {
                    clientes = dao.listarComFiltro(null, termo, null, null);
                }
        else if (campo == "CPF") {
                clientes = dao.listarComFiltro(null, null, termo, null);
            }
        else if (campo == "Telefone") {
            clientes = dao.listarComFiltro(null, null, null, termo);
        }
            else
            {
                clientes = dao.listarComFiltro(null, null, null, null);
            }
        
        insereListagemNaTabela(clientes);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tbListagemFuncMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListagemFuncMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int numLinhaSelecionada = (int) tbListagemFunc.getSelectedRow();
            int codFuncSelecionado = Integer.parseInt(tbListagemFunc.getValueAt(numLinhaSelecionada, 0).toString());

            IClienteDAO dao = new ClienteDAO();
            Cliente cliente = new Cliente();
            cliente = dao.buscarPorID(codFuncSelecionado);
            
            limpar();
            txtNome.setText(cliente.getNome());
            txtCPF.setText(cliente.getCPF());
            txtTelefone.setText(cliente.getTelefone());
            
            if (cliente.getDataNascimento()!= null) {
                SimpleDateFormat s = new SimpleDateFormat("dd/MM/yyyy");
                Calendar dataNasc = cliente.getDataNascimento();
                txtDataNasc.setText(s.format(dataNasc.getTime()));
            }
            

        }

    }//GEN-LAST:event_tbListagemFuncMouseClicked

