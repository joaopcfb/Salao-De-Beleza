    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        // TODO add your handling code here:
        
        Usuario usuario = new Usuario(txtUsuario.getText(), String.valueOf(txtSenha.getPassword()));
        IUsuarioDAO dao = new UsuarioDAO();

        if (dao.isLoginValido(usuario)) {
            
            Sessao.setIdUsuario( dao.pegarId(usuario.getUsuario()));
            fmPrincipal principal = new fmPrincipal();
            principal.setVisible(true);
            principal.setExtendedState(JFrame.MAXIMIZED_BOTH);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Usu√°rio ou senha incorretos");
        }
    }//GEN-LAST:event_btLoginActionPerformed

